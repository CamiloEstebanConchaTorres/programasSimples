# Escriba un programa que convierta de centímetros a pulgadas. Una pulgada es igual a 2.54 centímetros.

centimetros = float(input("ingrese el valor de la longitud \n"))
pulgadas = float(2.54)
conversion = centimetros / pulgadas
print (input(f"la conversion de la longitud {centimetros} cm nos da como resultado {conversion} in"))